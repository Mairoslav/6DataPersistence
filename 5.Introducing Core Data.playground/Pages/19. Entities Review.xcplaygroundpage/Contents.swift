// MARK: 19. Entities Review
// Quiz - An Entity is:
// a) A subclass of NSManagedObject. No, while classes that represent entities do need to subclass NSManagedObject, an entity is a template for a class, not a class itself.
// b) A way to define a single piece of data, including its attributes and relationships. YES, Entities are the basic building block of a Core Data model.
// c) An external store of your data. No, an entity only describes a piece of data; this definition is decoupled from how the data will ultimately be stored.

// b) so is correct

